#!/bin/bash
# ══════════════════════════════════════════════════════════════
# 정답 스크립트
# Phase 1 - Scenario 1.2: 커밋 메시지 수정 및 쪼개기/합치기
# ══════════════════════════════════════════════════════════════
# ⚠️ 주의: 이 스크립트는 참고용입니다.
# Interactive Rebase는 에디터 조작이 필요하므로 직접 실행할 수 없습니다.
# 각 Step을 하나씩 따라하세요.
# ══════════════════════════════════════════════════════════════


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# Part A: squash로 커밋 합치기
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

# ─── [Step 1: 현재 히스토리 확인] ─────────────────────
git log --oneline

# 예상 출력:
# <hash6> 아 또 수정
# <hash5> 진짜 수정
# <hash4> fix typo
# <hash3> wip
# <hash2> feat: 장바구니 UI 추가
# <hash1> init: 프로젝트 초기 설정

# ─── [Step 2: 각 커밋의 변경 내용 파악] ──────────────
git log --oneline --stat

# 어떤 커밋이 어떤 파일을 수정했는지 파악합니다.
# 커밋 3~6이 모두 cart.js를 수정하고 있음을 확인!

# ─── [Step 3: Interactive Rebase 시작] ────────────────
# 최근 4개 커밋(wip ~ 아 또 수정)을 대상으로 합니다.
git rebase -i HEAD~4

# ⚠️ [수동 작업 필요 - 에디터가 열립니다]
# 에디터에 다음과 같이 표시됩니다:
#
#   pick <hash3> wip
#   pick <hash4> fix typo
#   pick <hash5> 진짜 수정
#   pick <hash6> 아 또 수정
#
# 다음과 같이 수정하세요:
#
#   pick <hash3> wip
#   squash <hash4> fix typo
#   squash <hash5> 진짜 수정
#   squash <hash6> 아 또 수정
#
# 즉, 첫 번째는 pick으로 유지하고,
# 나머지를 모두 squash(또는 s)로 변경합니다.
#
# 저장하고 닫으세요 (vim: :wq / nano: Ctrl+X → Y → Enter)

# ⚠️ [두 번째 에디터가 열립니다 - 커밋 메시지 작성]
# 합쳐진 커밋의 메시지를 작성하는 에디터가 열립니다.
# 기존 메시지들이 모두 나열되어 있을 것입니다.
#
# 모두 지우고 다음과 같이 작성하세요:
#
#   feat: 장바구니 기능 구현
#
#   - 상품을 장바구니에 추가/삭제하는 기능
#   - 수량 관리 및 총합 계산
#   - 장바구니 UI 렌더링
#
# 저장하고 닫으세요.

# ─── [Step 4: 결과 확인] ─────────────────────────────
git log --oneline

# 예상 출력:
# <new_hash> feat: 장바구니 기능 구현
# <hash2> feat: 장바구니 UI 추가
# <hash1> init: 프로젝트 초기 설정

# 6개 → 3개로 줄었습니다!

# ─── [Step 5: 합쳐진 커밋 내용 확인] ─────────────────
git show HEAD --stat

# 예상 출력:
# feat: 장바구니 기능 구현
#  cart.js  | XX +++++++++++++++++++++++++++
#  style.css | XX +++++++++++++
#  2 files changed, XX insertions(+)

# ─── [Step 6: 코드가 정상인지 확인] ──────────────────
# cart.js의 최종 내용이 올바른지 확인
cat cart.js

# addToCart, removeFromCart, renderCart 함수가
# 모두 정상적으로 포함되어 있어야 합니다.


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# Part B: (심화) reword로 메시지만 수정하기
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

# ─── [Step 7: 두 번째 커밋의 메시지 수정] ─────────────
# "feat: 장바구니 UI 추가"를 더 구체적으로 바꿔봅시다.
git rebase -i HEAD~2

# ⚠️ [수동 작업 필요]
# 에디터에서:
#   reword <hash2> feat: 장바구니 UI 추가
#   pick <new_hash> feat: 장바구니 기능 구현
#
# 첫 줄의 pick을 reword(또는 r)로 변경합니다.
# 저장하고 닫으면 메시지 편집 에디터가 열립니다.
#
# 메시지를 수정하세요:
#   feat: 장바구니 페이지 HTML 구조 추가


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# Part C: (심화) 리베이스 취소하기
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

# ─── [도중에 실수했을 때] ─────────────────────────────
# 리베이스 도중 뭔가 잘못되었다면:
git rebase --abort

# 리베이스 시작 전 상태로 완벽하게 돌아갑니다.
# 안전하게 다시 시작할 수 있습니다.
